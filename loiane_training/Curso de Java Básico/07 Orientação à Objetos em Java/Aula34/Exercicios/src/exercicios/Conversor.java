public class Conversor {
    
    /*Exercicio 4*/
    public static double metersToFeet(double a) {
        return a * 10.76;
    }

    public static double feetToCentimeters(double a) {
        return a * 929;
    }

    public static double mileToAcres(double a) {
        return a * 640;
    }

    public static double acresToFeet(double a) {
        return a * 43.560;
    }

    /*Exercicio 6*/
    public static int minutesToSeconds(int a) {
        return a * 60;
    }

    public static int hoursToMinutes(int a) {
        return a * 60;
    }
    
    public static int daysToHours(int a){
        return a * 24;
    }
    
    public static int weeksToDays(int a){
        return a * 7;
    }
    
    public static int monthsToDays(int a){
        return a * 30;
    }
    
    public static double yearsToDays(int a){
        return a * 365.25;
    }

}
